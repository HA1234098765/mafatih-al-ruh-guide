// دالة للحصول على آية حسب الحالة النفسية
export const getVerseByMood = (mood: string) => {
  const verses = VERSES_BY_MOOD[mood as keyof typeof VERSES_BY_MOOD];
  if (!verses || verses.length === 0) {
    return VERSES_BY_MOOD.شكر[0]; // آية افتراضية
  }
  return verses[Math.floor(Math.random() * verses.length)];
};

// دالة للحصول على أذكار حسب الحالة والوقت
export const getAzkarByMoodAndTime = (mood: string, timing: string) => {
  return AZKAR_DATABASE.filter(dhikr => 
    (dhikr.mood.includes(mood) || mood === 'عادي') &&
    (dhikr.timing === timing || dhikr.timing === 'anytime' || timing === 'anytime')
  );
};

